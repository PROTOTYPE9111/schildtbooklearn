// Операция вычисления площади прямоугольника 
#include <iostream>

using namespace std;

int main() {
    int length; // Здесь объявляем переменную
    int width; // Вторая переменная 
    int area; // Третья переменная

    length = 7; 
    width = 5; 

    area = length * width; // Вычисляем S прямоугольника

    cout << "The area is:  ";
    cout << area; // Отображается число 35
}
